public class Programmieraufgabe4 {
    public static void main(String[] args) {
        int zeilen = 7;
        for (int i = zeilen; i >= 1; i--) {
            for (int j = 1; j <= i; j++) {
                System.out.print("*");
            }
            System.out.println();
        }
    }
    }













